#include "c8051_system.h"
#include "c8051_flash.h"
#include "c8051_i2c.h"
/************************************************
*	函数名称：SYSCLK_Init
*	功能描述：系统时钟频率的初始化
*	参    数：无
*   返 回 值：无
*   作    者: 
*************************************************/
void SYSCLK_Init(void)
{
	OSCICN |= 0x03; //配置内部振荡器的最大频率 不分频输出
	RSTSRC = 0x04;  //使能始终丢失检测寄存器
}